import java.util.ArrayDeque;

/**
 * Definition for singly-linked list.
 * public class ListNode {
 * int val;
 * ListNode next;
 * ListNode(int x) { val = x; }
 * }
 */
class Solution {
    public int[] reversePrint(ListNode head) {
        ArrayDeque<Integer> deque = new ArrayDeque<>();
        while (head != null) {
            deque.add(head.val);
            head = head.next;
        }
        int n = deque.size();
        int[] res = new int[n];
        int i = 0;
        for (int v : deque) {
            res[n - i - 1] = v;
            i++;
        }
        return res;
    }
}
