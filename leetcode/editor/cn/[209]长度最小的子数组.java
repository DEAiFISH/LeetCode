class Solution {
    public int minSubArrayLen(int s, int[] nums) {
        int lo = 1, hi = nums.length, min = 0;
        while (lo <= hi) {
            int mid = (lo + hi) >> 1;
            if (windowExist(mid, nums, s)) {
                hi = mid - 1;//æ‰¾åˆ°å°±ç¼©å°çª—å£çš„å¤§å°
                min = mid;//å¦‚æœæ‰¾åˆ°å°±è®°å½•ä¸‹æ¥
            } else
                lo = mid + 1;//æ²¡æ‰¾åˆ°å°±æ‰©å¤§çª—å£çš„å¤§å°
        }
        return min;
    }

    //sizeçª—å£çš„å¤§å°
    private boolean windowExist(int size, int[] nums, int s) {
        int sum = 0;
        for (int i = 0; i < nums.length; i++) {
            if (i >= size)
                sum -= nums[i - size];
            sum += nums[i];
            if (sum >= s)
                return true;
        }
        return false;
    }

}

