class Solution {
    public boolean isScramble(String s1, String s2) {
        char[] chs1 = s1.toCharArray();
        char[] chs2 = s2.toCharArray();
        int n = s1.length();
        int m = s2.length();
        if (n != m) {
            return false;
        }
        boolean[][][] dp = new boolean[n][n][n + 1];
        // åˆå§‹åŒ–å•ä¸ªå­—ç¬¦çš„æƒ…å†µ
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                dp[i][j][1] = chs1[i] == chs2[j];
            }
        }

        // æšä¸¾åŒºé—´é•¿åº¦ 2ï½n
        for (int len = 2; len <= n; len++) {
            // æšä¸¾ S ä¸­çš„èµ·ç‚¹ä½ç½®
            for (int i = 0; i <= n - len; i++) {
                // æšä¸¾ T ä¸­çš„èµ·ç‚¹ä½ç½®
                for (int j = 0; j <= n - len; j++) {
                    // æšä¸¾åˆ’åˆ†ä½ç½®
                    for (int k = 1; k <= len - 1; k++) {
                        // ç¬¬ä¸€ç§æƒ…å†µï¼šS1 -> T1, S2 -> T2
                        if (dp[i][j][k] && dp[i + k][j + k][len - k]) {
                            dp[i][j][len] = true;
                            break;
                        }
                        // ç¬¬äºŒç§æƒ…å†µï¼šS1 -> T2, S2 -> T1
                        // S1 èµ·ç‚¹ iï¼ŒT2 èµ·ç‚¹ j + å‰é¢é‚£æ®µé•¿åº¦ len-k ï¼ŒS2 èµ·ç‚¹ i + å‰é¢é•¿åº¦k
                        if (dp[i][j + len - k][k] && dp[i + k][j][len - k]) {
                            dp[i][j][len] = true;
                            break;
                        }
                    }
                }
            }
        }
        return dp[0][0][n];
    }
}
