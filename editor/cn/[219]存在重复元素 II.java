class Solution {
    public boolean containsNearbyDuplicate(int[] nums, int k) {
        HashMap<Integer, Integer> map = new HashMap<>();

        int len = nums.length;
        boolean flag = false;
        for (int i = 0; i < len; i++) {
            if (!map.containsKey(nums[i])) {
                map.put(nums[i], i);
            } else {
                Integer value = map.get(nums[i]);
                if (Math.abs(i - value) <= k) {
                    return true;
                } else {
                    map.replace(nums[i], i);
                }
            }

        }
        return false;
    }
}
